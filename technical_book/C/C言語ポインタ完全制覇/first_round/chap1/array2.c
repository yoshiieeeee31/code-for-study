#include <stdio.h>

int	main(void)
{
	int		array[5];
	int		*p;
	int		i;

	i = 0;
	while (i < 5)
	{
		array[i] = i;
		i++;
	}
	p = array;
	while (p != &array[5])
	{
		printf("%d\n", *p);
		p++;
	}
	return (0);
}
